// we define using two keywords , let and const : 
// variables once defined cannot be redefined

// variables defined using let are mutable :  the value referenced can be changed
//  when coming up with the name for a variable make it descriptive as
// possible 
// an equal sign is used to 'assign' a value to a variable name. 
